package Aoc13;

public class ClawMachine {
    Long ax;
    Long ay;
    Long bx;
    Long by;
    Long targetx;
    Long targety;

    public ClawMachine(Long ax, Long ay, Long bx, Long by, Long targetx, Long targety) {
        this.ax = ax;
        this.ay = ay;
        this.bx = bx;
        this.by = by;
        this.targetx = targetx;
        this.targety = targety;
    }
}
